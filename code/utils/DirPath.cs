namespace Sandbox.Utils;

public class DirPath
{
	/// <summary>
	/// This methods returns the Character folder path
	/// </summary>
	/// <returns>String</returns>
	public static string GetCharacterPath()
	{
		return "/character";
	}

	
	/// <summary>
	/// This methods returns the Entities folder path
	/// </summary>
	/// <returns>String</returns>
	public static string GetEntitiesPath()
	{
		return "/entities";
	}

	
	/// <summary>
	/// This methods returns the Tools folder path
	/// </summary>
	/// <returns>String</returns>
	public static string GetToolPath()
	{
		return "/tools";
	}

	
	/// <summary>
	/// This methods returns the UI folder path
	/// </summary>
	/// <returns>String</returns>
	public static string GetUiPath()
	{
		return "/ui";
	}
	
	
	/// <summary>
	/// This methods returns the Utilities folder path
	/// </summary>
	/// <returns>String</returns>
	public static string GetUtilsPath()
	{
		return "/utils";
	}

	
	/// <summary>
	/// This methods returns the Weapons folder path
	/// </summary>
	/// <returns>String</returns>
	public static string GetWeaponsPath()
	{
		return "/weapons";
	}
}
